    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel6.setVisible(false);
        if(jTextField1.getText().length()<1 || jTextField2.getText().length()<1
                ||Integer.parseInt(jSpinner1.getValue().toString())==0)
        {
            jLabel6.setText("Please Fill All The Field");
            jLabel6.setVisible(true);
            return;
        }
        String ti=jTextField1.getText();
        String au=jTextField2.getText();
        String py=jComboBox3.getSelectedItem().toString();
        int count=Integer.parseInt(jSpinner1.getValue().toString());
       
        try {
            boolean check=LMSClient.look_up.addBook(ti, au, py, count);
            if(check)
            {
                JOptionPane.showMessageDialog(null, "The Book has been added successfully");
                clear();
            }
            else
                JOptionPane.showMessageDialog(null, "The Book Is Already Exist");
        } catch (RemoteException ex) {
            Logger.getLogger(NEW_BOOK.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

