    public void loadBooks() throws RemoteException
    {
        model = (DefaultTableModel) jTable1.getModel();
        for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
                model.removeRow(i);
        }
        ArrayList<ArrayList<String>> bDet = LMSClient.look_up.getBookDetails();
        for(int i=0;i<bDet.size();i++)
        {
            int count=Integer.parseInt(bDet.get(i).get(3));
            String name=bDet.get(i).get(0);
            int out=LMSClient.look_up.getBc(name);
            count-=out;
            bDet.get(i).set(3, count+"");
        }
        for(int i=0;i<bDet.size();i++)
        {
            Object[] row = { bDet.get(i).get(0), bDet.get(i).get(1), bDet.get(i).get(2), bDet.get(i).get(3) };
            model.addRow(row);
        }
    }
   
