    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try
        {
            int column = 0;
            int row = jTable1.getSelectedRow();
            String value = jTable1.getModel().getValueAt(row, column).toString();
            int avCount=Integer.parseInt(jTable1.getModel().getValueAt(row, 3).toString());
            if(avCount<1)
            {
                JOptionPane.showMessageDialog(null, "There Is No Available Copy Of" +value);
                return;
            }
            ArrayList<String> Ids=LMSClient.look_up.loadchID(userName);
            if(Ids.size()>3)
            {
                JOptionPane.showMessageDialog(null, "You Already Borrowed 4 Books");
                return;
            }
                
            LMSClient.look_up.borrowBook(value, userName);
            JOptionPane.showMessageDialog(null, "The Book has been checked out successfully");
            loadBooks();
            
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Please Select Book To Borrow");
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

