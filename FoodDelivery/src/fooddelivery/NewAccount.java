    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel5.setVisible(false);
        if(jTextField1.getText().length()<1
                ||jPasswordField1.getText().length()<1)
        {
            jLabel5.setText("Please Fill All Field.");
            jLabel5.setVisible(true);
            return;
        }
        if(!jPasswordField1.getText().equals(jPasswordField2.getText()))
        {
            jLabel5.setText("Passwords Are Not Match.");
            jLabel5.setVisible(true);
            return;
        }
        try
        {
            String Ip="127.0.0.1";
            int port=5217;
            ClientSoc=new Socket(Ip,port);
            din=new DataInputStream(ClientSoc.getInputStream());
            dout=new DataOutputStream(ClientSoc.getOutputStream());
            br=new BufferedReader(new InputStreamReader(System.in));
            dout.writeUTF("NewUser%"+jTextField1.getText()+"%"+jPasswordField1.getText());
            String serializedResponse=din.readUTF();
            if(!serializedResponse.contains("OK"))
            {
                jLabel5.setVisible(true);
                jLabel5.setText("The User Already Exist.");
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(null,"The User Created Successfully.");
                this.setVisible(false);
            }
            
        }
        catch(Exception ex)
        {
            jLabel5.setVisible(true);
         jLabel5.setText("Failed to connect to server.");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


