    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel8.setVisible(false);
        jLabel11.setVisible(false);
        String module = jTextField1.getText();
        String assignment = jTextField2.getText();
        String bio = jTextField3.getText();
        String status;
        int id;
        int bioID;
        if(jTextField1.getText().isEmpty() ||jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() )
        {
            jLabel11.setVisible(true);
        }
        else{
            
        try{
          
            if (le.CheckAssignment(assignment)==0){
                jLabel8.setVisible(true);
            }
            else
            {
                
                id=le.CheckAssignment(assignment);
                if(le.CheckBibo(bio)==0){
                    jLabel9.setText("Not Available-Requested");
                    status="Req";
                    
                }
                else{
                    bioID=le.CheckBibo(bio);
                    jLabel9.setText("Available- Bibo ID:"+bioID);
                    status=bioID+"";
                    
                }
                le.add(id, module, assignment, bio, status);
                jLabel7.setText(id+"");

            }
        }catch(Exception e){
              
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

