    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel5.setVisible(false);
        jLabel6.setVisible(false);
        jLabel7.setVisible(false);
        if ( !jPasswordField1.getText().toString().equals(jPasswordField2.getText().toString())){
            jLabel6.setVisible(true);
        }
        else if ( jTextField1.getText().isEmpty()||jPasswordField1.getText().isEmpty()){
            jLabel7.setVisible(true);
        }
        else{
            String username , password , accessuser="";
            username = jTextField1.getText().toString();
            password = jPasswordField1.getText().toString();
            
            switch (jComboBox1.getSelectedIndex()){
                case 0:
                    accessuser = "st";
                    break;
                case 1:
                    accessuser = "li";
                    break;
                case 2:
                    accessuser = "le";
                    break;
                case 3:
                    accessuser = "ad";
                    break;
            }
            boolean check;
            try {
                User Uobject = new User();
                check = Uobject.CheckUserName(username);
                if ( check == false){
                    jLabel5.setVisible(true);
                }
                else {
                    Uobject.AddUser(username, password, accessuser);
                }
            } catch (IOException ex) {
                Logger.getLogger(NewUserAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

