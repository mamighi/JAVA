    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel8.setVisible(false);
        if(jComboBox1.getSelectedItem().toString().equals("User Name") ||jTextField2.getText().isEmpty()
                ||jTextField4.getText().isEmpty() || jPasswordField1.getText().isEmpty())
        {
            jLabel8.setText("Please Insert All Information");
            jLabel8.setVisible(true);
            return;
        }
        if(!jPasswordField1.getText().equals(jPasswordField2.getText()))
        {
            jLabel8.setText("Passwords Are Not Matched");
            jLabel8.setVisible(true);
            return;
        }
        
       for(int i=0;i<arrayList.size();i++)
        {
            if(arrayList.get(i).get(0).equals(jComboBox2.getSelectedItem().toString()))
            {
                ArrayList<String> temp= new ArrayList<String>();
                temp.add(jComboBox2.getSelectedItem().toString());
                temp.add(jPasswordField1.getText());
                temp.add(jTextField2.getText());
                temp.add(jTextField4.getText());
                temp.add(jComboBox1.getSelectedItem().toString());
                arrayList.set(i, temp);
                break;
            }
        }
        BufferedWriter fw = null;
            try {
                fw = new BufferedWriter(new FileWriter(Users,false));
            } catch (IOException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        for(int i=0;i<arrayList.size();i++)
        {

            try {
                fw.write(arrayList.get(i).get(0)+" "+arrayList.get(i).get(1)+" "+arrayList.get(i).get(2)
                        +" "+arrayList.get(i).get(3)+" "+arrayList.get(i).get(4));
            } catch (IOException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                fw.newLine();
            } catch (IOException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        try {
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "The user has been updated successfully");
            this.setVisible(false);
       
     
    }//GEN-LAST:event_jButton1ActionPerformed

