    public void getAllusers()
    {
        arrayList.clear();
        if(!Users.exists())
            return;
         try{
               Scanner fs;
               fs= new Scanner(Users);
               while (fs.hasNext()){
                   ArrayList<String> temp= new ArrayList<String>();
                   temp.add(solvePer(fs.next()));
                   temp.add(solvePer(fs.next()));
                   temp.add(solvePer(fs.next()));
                   temp.add(solvePer(fs.next()));
                   temp.add(solvePer(fs.next()));
                   arrayList.add(temp);
               }
               fs.close();

          }
         catch(Exception e){
           }
         jComboBox2.removeAllItems();
         jComboBox2.addItem("User Name");
         for(int i=0;i<arrayList.size();i++)
         {
             jComboBox2.addItem(arrayList.get(i).get(0));
         }
    }
