    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel8.setVisible(false);
        if(jTextField2.getText().isEmpty()
                ||jComboBox1.getSelectedItem().toString().equals("Supplier"))
        {
            jLabel8.setText("Please Insert All Information");
            jLabel8.setVisible(true);
            return;
        }
        for(int i=0;i<arrayListItem.size();i++)
        {
            if(arrayListItem.get(i).get(0).equals(jComboBox2.getSelectedItem().toString()))
            {
                ArrayList<String> temp= new ArrayList<String>();
                temp.add(jComboBox2.getSelectedItem().toString());
                temp.add(jTextField2.getText());
                temp.add(jComboBox1.getSelectedItem().toString());
                arrayListItem.set(i, temp);
                break;
            }
        }

       try{

            BufferedWriter fw= new BufferedWriter(new FileWriter(Item,false));
            for(int j=0;j<arrayListItem.size();j++)
            {
                fw.write(solveSpace(arrayListItem.get(j).get(0))+" "+
                        solveSpace(arrayListItem.get(j).get(1))+" "+solveSpace(arrayListItem.get(j).get(2)));
                fw.newLine();
            }
            fw.close();
            JOptionPane.showMessageDialog(null, "The item has been updated successfully");
            this.setVisible(false);
        }catch(Exception e){   
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

