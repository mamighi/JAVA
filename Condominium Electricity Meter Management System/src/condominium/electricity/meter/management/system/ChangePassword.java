    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            TaskManager ts= new TaskManager();
            String UserName=jTextField1.getText();
            String Password=jPasswordField1.getText();
            if(!ts.login(UserName, Password))
            {
                jLabel2.setText("Wrong User Name Or Password");
                jLabel2.setVisible(true);
                return;
            }
            if(!jPasswordField2.getText().equals(jPasswordField3.getText()))
            {
                jLabel2.setText("New Passwords Are Not Match");
                jLabel2.setVisible(true);
                return;
            }
            if(jPasswordField2.getText().length()<3)
            {
                jLabel2.setText("New Passwords Atleast Should Be 3 Characters");
                jLabel2.setVisible(true);
                return;
            }
            ts.changePass(UserName, jPasswordField2.getText());
              JOptionPane.showMessageDialog (null, "The Password Has Been Updated Successffuly.", "Success", JOptionPane.ERROR_MESSAGE);
              this.setVisible(false);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


