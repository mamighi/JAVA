    public void loadTable(String UnitNo)
    {
         jTable1.removeAll();
         jLabel3.setText(UnitNo);
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         model.getDataVector().removeAllElements();
         TaskManager ts= new TaskManager();
        model.setRowCount(0);
        try {
            // TODO add your handling code here:
            ArrayList<ArrayList<Integer>> details=ts.getUsages(UnitNo);
           
           
            for(int i=0;i<details.size();i++)
            {
                model.addRow(new Object[]{i+1,details.get(i).get(0), details.get(i).get(1), 
                 details.get(i).get(2)});
            }
        }
        catch(Exception ex){}
    }

