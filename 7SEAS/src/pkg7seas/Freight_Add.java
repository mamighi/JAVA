    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel10.setVisible(false);
        if(jComboBox2.getSelectedItem().toString().equals("ROUTS")
           || jTextField4.getText().toString().equals("0"))
        {
            jLabel10.setText("Insert All The Details");
            jLabel10.setVisible(false);
            return;
        }
        String cus=jComboBox1.getSelectedItem().toString().replace(' ', '?');
        String ro=jComboBox2.getSelectedItem().toString().replace(' ', '?');
        String cl=jComboBox3.getSelectedItem().toString().replace(' ', '?');
        String ports=jTextField1.getText().replace(' ', '?');
        String dis=jTextField2.getText();
        String typ=jComboBox4.getSelectedItem().toString();
        String we=jTextField3.getText();
        String pr=jTextField4.getText();
        
        try
        {
            BufferedWriter bw = new BufferedWriter( new FileWriter (Fre , true));
            bw.write(cus.replace(' ', '?')+" "+ ro.replace(' ', '?')+ " " +cl.replace(' ', '?')+" "+ports.replace(' ', '?')+" "+dis+" "+typ+" "+we+" "+pr);
            bw.newLine();
            bw.close();
        }
        catch(Exception ex){}
        
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        JOptionPane.showMessageDialog(this, "The New Client Has Been Added Successfuly");
        this.setVisible(false);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

