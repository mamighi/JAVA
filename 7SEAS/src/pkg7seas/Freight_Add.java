    private void jTextField3KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyTyped
        // TODO add your handling code here:
                       int w=0;
                       jLabel10.setVisible(false);
        try
        {
            w=Integer.parseInt(jTextField3.getText().toString());
        }catch(Exception ex){
            jLabel10.setText("Invalid Weight");
            jTextField4.setText("0");
            jLabel10.setVisible(true);
            return;
        }
        if(w<=7000)
            jLabel11.setText("LTL");
        else
            jLabel11.setText("TL");
        /*
            Conteiner 0.01 0.015
            Barge     0.02 0.025
            Tankers   0.03 0.035  
            Tugboats  0.04 0.045
            Cargo     0.05 0.055
            Cruses    0.06 0.065
        */
        int dis=0;
        try
        {
            dis=Integer.parseInt(jTextField2.getText().toString());
        }
        catch(Exception ex){return;}
        double rate=0;
        if(jComboBox4.getSelectedItem().toString().equals("Conteiner")) rate=0.01;
        else if(jComboBox4.getSelectedItem().toString().equals("Barge")) rate=0.02;
        else if(jComboBox4.getSelectedItem().toString().equals("Tankers")) rate=0.03;
        else if(jComboBox4.getSelectedItem().toString().equals("Tugboats")) rate=0.04;
        else if(jComboBox4.getSelectedItem().toString().equals("Cargo")) rate=0.05;
        else rate=0.06;
        if(jLabel11.getText().equals("LTL")) rate+=0.005;
        double price=rate*dis*w;
        jTextField4.setText(price+"");
    }//GEN-LAST:event_jTextField3KeyTyped


