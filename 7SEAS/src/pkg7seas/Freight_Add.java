    public void loadCustomer() throws FileNotFoundException
    {
        Scanner FileScanner = new Scanner(Cus);
        String name , ph;
        while ( FileScanner.hasNext()){
            name = FileScanner.next();
            name=name.replace('?', ' ');
            ph = FileScanner.next();
            jComboBox1.addItem(name);
        }
        FileScanner.close();
    }
    public void loadRDet() throws FileNotFoundException
    {
        Scanner FileScanner = new Scanner(Routes);
        String ro,port,ship,dis;
        while ( FileScanner.hasNext()){
            ro = FileScanner.next();
            ro=ro.replace('?', ' ');
            port = FileScanner.next();
            port=port.replace('?', ' ');
            port=port.replace('-', ',');
            ship=FileScanner.next();
            dis=FileScanner.next();
            ArrayList<String> Det=new  ArrayList<String>();
            Det.add(ro);
            Det.add(port);
            Det.add(ship);
            Det.add(dis);
            rDet.add(Det);
        }
        FileScanner.close();
    }
    public void loadClientDet() throws FileNotFoundException
    {
        Scanner FileScanner = new Scanner(Client);
        String name , service , type;
        while ( FileScanner.hasNext()){
            name = FileScanner.next();
            name=name.replace('?', ' ');
            service = FileScanner.next();
            type = FileScanner.next();
            ArrayList<String> det=new ArrayList<String>();
            det.add(name);
            det.add(service);
            det.add(type);
            details.add(det);
        }
        FileScanner.close();
    }

