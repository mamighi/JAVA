    public boolean checkUserExistance(String name_) throws FileNotFoundException
    {
        name_=name_.replace(' ', '?');
         Scanner FileScanner = new Scanner(Client);
        String name , service , type;
        while ( FileScanner.hasNext()){
            name = FileScanner.next();
            service = FileScanner.next();
            type = FileScanner.next();
            if ( name.equals(name_)){
                FileScanner.close();
                return true;
            }
        }
        FileScanner.close();
        return false;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel5.setVisible(false);
        try
        {
        if(checkUserExistance(jTextField1.getText()))
        {
            jLabel5.setText("Client Already Exist");
            jLabel5.setVisible(true);
            return;
        }
        }
        catch(Exception ex){}
        if(jTextField1.getText().length()<1 ||
                (!jCheckBox1.isSelected() && !jCheckBox2.isSelected()
                && !jCheckBox3.isSelected() &&!jCheckBox4.isSelected()
                &&!jCheckBox5.isSelected() && !jCheckBox6.isSelected())||
                jPasswordField1.getText().length()<1)
        {
            jLabel5.setText("Enter All The Information");
            jLabel5.setVisible(true);
            return;
        }
        String name=jTextField1.getText();
        name=name.replace(' ', '?');
        String service="";
        if(jCheckBox1.isSelected()) service+="-1";
        if(jCheckBox2.isSelected()) service+="-2";
        if(jCheckBox3.isSelected()) service+="-3";
        if(jCheckBox4.isSelected()) service+="-4";
        if(jCheckBox5.isSelected()) service+="-5";
        if(jCheckBox6.isSelected()) service+="-6";
        String type="";
        if(jRadioButton1.isSelected()) type="Contract";
        else type="Operating";
        try
        {
        BufferedWriter bw = new BufferedWriter( new FileWriter (Client , true));
        bw.write(name+" "+ service+ " " + type);
        bw.newLine();
        bw.close();
        bw = new BufferedWriter( new FileWriter (User , true));
        bw.write(name+" "+ jPasswordField1.getText()+ " " + "cl");
        bw.newLine();
        bw.close();
        
        }
        catch(Exception ex){}
        
        
        jTextField1.setText("");
        JOptionPane.showMessageDialog(this, "The New Client Has Been Added Successfuly");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

