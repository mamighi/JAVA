        // TODO add your handling code here:
        jLabel5.setVisible(false);
        try
        {
        if(checkUserExistance(jTextField1.getText()))
        {
            jLabel5.setText("Customer Already Exist");
            jLabel5.setVisible(true);
            return;
        }
        }
        catch(Exception ex){}
        if(jTextField1.getText().length()<1 ||
                jPasswordField1.getText().length()<1)
        {
            jLabel5.setText("Enter All The Information");
            jLabel5.setVisible(true);
            return;
        }
        String name=jTextField1.getText();
        name=name.replace(' ', '?');
        String ph=jTextField2.getText().toString();
        
        try
        {
        BufferedWriter bw = new BufferedWriter( new FileWriter (Cus , true));
        bw.write(name+" "+ ph);
        bw.newLine();
        bw.close();
         bw = new BufferedWriter( new FileWriter (User , true));
        bw.write(name+" "+ jPasswordField1.getText()+ " " + "cl");
        bw.newLine();
        bw.close();
        }
        catch(Exception ex){}
        jTextField1.setText("");
        jTextField2.setText("");
        JOptionPane.showMessageDialog(this, "The New Client Has Been Added Successfuly");
        this.setVisible(false);
