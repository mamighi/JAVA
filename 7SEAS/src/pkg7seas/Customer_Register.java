    public boolean checkUserExistance(String name_) throws FileNotFoundException
    {
        name_=name_.replace(' ', '?');
         Scanner FileScanner = new Scanner(Cus);
        String name , ph;
        while ( FileScanner.hasNext()){
            name = FileScanner.next();
            ph = FileScanner.next();
            if ( name.equals(name_)){
                FileScanner.close();
                return true;
            }
        }
        FileScanner.close();
        return false;
    }
