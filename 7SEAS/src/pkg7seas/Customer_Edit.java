    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel5.setVisible(false);
        if(jTextField2.getText().length()<1
               )
        {
            jLabel5.setText("Enter All The Information");
            jLabel5.setVisible(true);
            return;
        }
        String name=jComboBox1.getSelectedItem().toString();
        name=name.replace(' ', '?');
        String ph=jTextField2.getText().toString();
        try
        {
        Scanner FileScanner = new Scanner(Cus);
        BufferedWriter bw = new BufferedWriter( new FileWriter (tCus , false));
        String n , p;
        while ( FileScanner.hasNext()){
            n = FileScanner.next();
            p = FileScanner.next();
            if(n.equals(name))
            {
                bw.write(n+" "+ ph);
            }
            else
             bw.write(n+" "+ p);
             bw.newLine();
        }
        bw.close();
        FileScanner.close();
        FileScanner = new Scanner(tCus);
        bw = new BufferedWriter( new FileWriter (Cus , false));

        while ( FileScanner.hasNext()){
            n = FileScanner.next();
            p = FileScanner.next();
            
            bw.write(n+" "+ p);
            bw.newLine();
        }
        bw.close();
        FileScanner.close();
        }catch(Exception ex){}
        

        jTextField2.setText("");
        JOptionPane.showMessageDialog(this, "The Client Information Has Been Updated Successfully.");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

