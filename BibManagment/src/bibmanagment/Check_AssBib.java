     public void fillmod() throws FileNotFoundException{
        jComboBox1.removeAllItems();
        Scanner fs = new Scanner(ass);
        while(fs.hasNext()){
            fs.next();
            
            jComboBox1.addItem(fs.next());
            fs.next();
            fs.next();

        }
        fs.close();
    }
    public void fillass() throws FileNotFoundException{
        jComboBox2.removeAllItems();
        String m= jComboBox1.getSelectedItem().toString();
        Scanner fs = new Scanner(ass);
        while(fs.hasNext())
        {
            fs.next();
            
            
            if(fs.next().equals(m))
            {
                jComboBox2.addItem(fs.next());
            }
            else
                fs.next();
            fs.next();       
        }
    }

