    public int getbibid() throws FileNotFoundException{
        String m= jComboBox1.getSelectedItem().toString();
        String a= jComboBox2.getSelectedItem().toString();
        int id=0;
        Scanner fs = new Scanner(ass);
        while(fs.hasNext()){
            id=fs.nextInt();
            
            String cm= fs.next();
            String ca=fs.next();
            if(cm.equals(m) && ca.equals(a))
                return id;
            fs.next();            
        }
        fs.close();
        return id;
        
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            int id=getbibid();
            int chid;
            String title ,author ,publisher ,pdate ,pages ,location,av;
            File bio = new File("bio.txt");
            Scanner fs= new Scanner(bio);
            while (fs.hasNext()){
                chid=fs.nextInt();
                title= fs.next();
                author=fs.next();
                publisher=fs.next();
                pdate=fs.next();
                pages=fs.next();
                location=fs.next();
                av=fs.next();
                if(chid==id){
                    jPanel1.setVisible(true);
                    jTextField1.setText(title);
                    jTextField2.setText(author);
                    jTextField3.setText(publisher);
                    jTextField4.setText(pdate);
                    jTextField5.setText(pages);
                    jTextField6.setText(location);
                    jLabel13.setText(chid+"");
                    if(av.equals("NO"))
                    {
                        jButton2.setEnabled(false);
                    }
                    else
                        jButton2.setEnabled(true);

                }
            }

        }catch(Exception e){

        }

    }//GEN-LAST:event_jButton1ActionPerformed

