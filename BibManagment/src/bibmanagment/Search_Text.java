    public void search() throws FileNotFoundException, IOException
    {
        Scanner Filescanner = new Scanner( Bio );
        String title,author,publisher,date,page,location,AV;
        int id=0;
        boolean found=false;
        while (Filescanner.hasNext()){
            id= Filescanner.nextInt();
            title = Filescanner.next();
            author = Filescanner.next();
            publisher = Filescanner.next();
            date = Filescanner.next();
            page = Filescanner.next();
            location = Filescanner.next();
            AV = Filescanner.next();
            if(AV.equals("AV"))
            {
                if(readTxtFile(id))
                {
                    jTextField7.setText(id+"");
                    jTextField1.setText(title);
                    jTextField2.setText(author);
                    jTextField3.setText(publisher);
                    jTextField4.setText(date);
                    jTextField5.setText(page);
                    jTextField6.setText(location);
                    found=true;
                    break;
                }
            }

        }
        Filescanner.close();
        if(!found)
        {
            jLabel2.setVisible(true);
            jLabel2.setText("Could Not Find.");
        }
    }
    public boolean readTxtFile(int id) throws FileNotFoundException, IOException
    {
        String FileName=id+".txt";
        File text= new File(FileName);
        if(!text.exists())
            return false;
    
        FileInputStream fis = new FileInputStream(text);
        byte[] data = new byte[(int) text.length()];
        fis.read(data);
        fis.close();
        String content = new String(data, "UTF-8");
        if(content.contains(jTextArea1.getText()))
            return true;
        return false;
        
        
    }

