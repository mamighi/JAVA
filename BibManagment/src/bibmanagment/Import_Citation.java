    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        model.removeAllElements();
        try
        {
            String text=jTextArea1.getText();
            String[] cites = text.split("-");
                 Scanner fs = new Scanner(Bio);
            for(int i=0;i<cites.length;i++)
            {
                String[] wCites=cites[i].split(",");
           
                int id ;
                String ti,au,pub,date,page,loc;
                String Check;
                while (fs.hasNext()){
                    id = fs.nextInt();
                    ti = fs.next();
                    au=fs.next();
                    pub=fs.next();
                    date=fs.next();
                    page=fs.next();
                    loc=fs.next();
                    fs.next();
                    if(wCites.length==2)
                    {
                        if(wCites[0].equals(ti) && wCites[1].equals(date))
                        {
                            gen+=au+", "+date+", "+ti+", "+pub+", "+loc+"\n\n";
                            model.addElement(ti);
                            
                        }
                    }

                }    
            }
           fs.close();
        }
        catch(Exception ex){}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!model.elements().hasMoreElements()) 
        {
            JOptionPane.showMessageDialog(this, "No Source Found.");
            return;
        }
        
        
        try {
             // TODO add your handling code here:
             JFileChooser fch= new JFileChooser();
             fch.showSaveDialog(this);
             FileFilter ff;
             File selectedFile = fch.getSelectedFile();
             
             final Formatter F = new Formatter (selectedFile);
             F.close();
             BufferedWriter bw = new BufferedWriter(new FileWriter(selectedFile,true));
             bw.write(gen);
             bw.close();
         } catch (FileNotFoundException ex) {
             Logger.getLogger(Generate_Bib.class.getName()).log(Level.SEVERE, null, ex);
         } catch (IOException ex) {
             Logger.getLogger(Generate_Bib.class.getName()).log(Level.SEVERE, null, ex);
         }
         JOptionPane.showMessageDialog(this, "The file generated successfully.");
         clear();
         this.setVisible(false);
        
    }//GEN-LAST:event_jButton2ActionPerformed

