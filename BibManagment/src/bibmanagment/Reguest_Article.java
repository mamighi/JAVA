     public void loadRq() throws FileNotFoundException
    {
        listModel.removeAllElements();
        Scanner Filescanner = new Scanner( req );
        String ti;
        int id=0;
        while (Filescanner.hasNext()){
            id= Filescanner.nextInt();
            ti = Filescanner.next().replaceAll("%", " ");
            listModel.addElement(ti);
            for ( int i = 0 ; i<5 ; i++){
                Filescanner.next();
            }
        }
        Filescanner.close();
    }
    public void addReq( int id,String title , String author , String publisher , String pdate , String location ) throws IOException{
        title=title.replaceAll(" ", "%");
        author=author.replaceAll(" ", "%");
        publisher=publisher.replaceAll(" ", "%");
        pdate=pdate.replaceAll(" ", "%");
        location=location.replaceAll(" ", "%");
        BufferedWriter bw = new BufferedWriter(new FileWriter(req,true));
        bw.write(id+" "+title + " " + author + " " + publisher + " " + pdate + " " + location+" "+"NO");
        bw.newLine();
        bw.close();
        
    }
    
    public int checkTitle ( String Title ) throws FileNotFoundException{
         Title=Title.replaceAll(" ", "%");
        Scanner Filescanner = new Scanner( req );
        String ti;
        int id=0;
        while (Filescanner.hasNext()){
            id= Filescanner.nextInt();
            ti = Filescanner.next();
            for ( int i = 0 ; i<5 ; i++){
                Filescanner.next();
            }
            if ( ti.equals(Title)){
                Filescanner.close();
                return 0;
            }
            
        }
        Filescanner.close();
        return id+1;
    }
