    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jLabel15.setVisible(false);
         if(!jPasswordField3.getText().equals(jPasswordField4.getText()))
        {
            jLabel15.setText("Password are not match");
            jLabel15.setVisible(true);
            return;
        }
        if(jPasswordField3.getText().length()<1||
                jTextField5.getText().length()<1 || jTextField6.getText().length()<1)
        {
            jLabel15.setText("Please fill all the information.");
            jLabel15.setVisible(true);
            return;
        }
        String un=jComboBox3.getSelectedItem().toString();
        String pass=jPasswordField3.getText();
        String fn=jTextField5.getText();
        String ln= jTextField6.getText();
        String gen=jComboBox2.getSelectedItem().toString();
        
        Connection c = null;
        try
        {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:LMS.db");
             Statement statement = null;  
            statement = c.createStatement();  
            statement.executeUpdate("UPDATE USER SET Password='"+pass+"', FN='"+fn+"',LN='"+ln+"',Gen='"+gen+"' WHERE UserName='"+un+"'");
            c.close();
            JOptionPane.showMessageDialog(null, "The Librarian has been updated successfully");
            this.setVisible(false);
        
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

