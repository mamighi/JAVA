    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String userName=jTextField1.getText();
        String Password=jPasswordField1.getText();
        String Intake=jComboBox1.getSelectedItem().toString();
        if(userName.length()<1 || Password.length()<1 || Intake.length()<1)
        {
            jLabel7.setText("Please Insert UserName And Password.");
            jLabel7.setVisible(true);
            return;
        }
        if(!Password.equals(jPasswordField2.getText()))
        {
            jLabel7.setText("Passwords Do Not Match.");
            jLabel7.setVisible(true);
            return;
        }
        try
        {
            File user= new File("User.txt");
            Scanner fs= new Scanner(user);
            boolean retVal=false;
            while(fs.hasNext())
            {
                String usNa=fs.next();
                String pa=fs.next();
                String usTy=fs.next();
                if(usNa.equals(userName))
                retVal=true;
            }
            fs.close();
            if(retVal)
            {
                jLabel7.setText("User Already Exist!");
                jLabel7.setVisible(true);
                return;
            }
            else
            {
                BufferedWriter fw= new BufferedWriter(new FileWriter(user,true));
                fw.write(userName+" "+Password+" "+"ST");
                fw.newLine();
                fw.close();
                File Student= new File("Student.txt");
                fw= new BufferedWriter(new FileWriter(Student,true));
                fw.write(userName+" "+Intake);
                fw.newLine();
                fw.close();
                JOptionPane.showMessageDialog(this,"The new student has been added successfully");
                this.setVisible(false);
            }

        }catch(Exception ex){}
    }//GEN-LAST:event_jButton1ActionPerformed

