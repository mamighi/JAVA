    public void init(String user)
    {
        User=user;
        jComboBox2.removeAllItems();
        File subject= new File("subject.txt");
        try
        {
        Scanner fs= new Scanner(subject);
        while(fs.hasNext())
        {
             String tSub=fs.next();
             String tIntake=fs.next();
             String tLec=fs.next();
             if(tLec.equals(User))
                  jComboBox2.addItem(tSub);
        }
        fs.close();
        }catch(Exception ex){}
        
        if(jComboBox2.getItemCount()<1)
        {
            JOptionPane.showMessageDialog(this, "There Is No Subject Assigned To You!");
            this.setVisible(false);
        }
        
        loadStudent(jComboBox2.getSelectedItem().toString());
    }
    public void loadStudent(String sub)
    {
        File subject= new File("subject.txt");
        String Intake="";
        try
        {
        Scanner fs= new Scanner(subject);
        while(fs.hasNext())
        {
             String tSub=fs.next();
             String tIntake=fs.next();
             String tLec=fs.next();
             if(sub.equals(tSub))
                  Intake=tIntake;
        }
        fs.close();
        }catch(Exception ex){}
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        try
        {
            File Student= new File("Student.txt");
            Scanner fs= new Scanner(Student);
            boolean retVal=false;
            while(fs.hasNext())
            {
                String usNa=fs.next();
                String intake=fs.next();
                if(Intake.equals(intake))
                    model.addRow(new Object[]{usNa,"ABSENT"});
            }
            fs.close();
        }catch(Exception ex){}
    }
    public void updateAtt(String st)
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int index=jTable1.getSelectedRow();
        model.setValueAt(st, index, 1);
        
    }

