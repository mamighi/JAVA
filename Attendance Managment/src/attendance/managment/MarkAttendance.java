    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String time=jComboBox6.getSelectedItem().toString();
        time=time.replace(":","_");
       
        String FileName=jComboBox2.getSelectedItem().toString()+"_"+
                jComboBox3.getSelectedItem().toString()+"_"+
                jComboBox4.getSelectedItem().toString()+"_"+
                jComboBox5.getSelectedItem().toString()+"_"+
                time+".txt";
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        File theDir = new File("ATT");
        if (!theDir.exists()) {
              try{
                theDir.mkdir();
            } 
            catch(SecurityException se){
                //handle it
            }       
        }
        File att= new File(theDir+"\\"+FileName);
        if(!att.exists())
        {
            try
            {
                final Formatter y;
                y= new Formatter (theDir+"\\"+FileName);
                y.close();
                BufferedWriter fw= new BufferedWriter(new FileWriter(att,true));
                for(int i=0;i<jTable1.getRowCount();i++)
                {
                    fw.write(model.getValueAt(i, 0)+" "+model.getValueAt(i, 1)+" "+"NONE");
                    fw.newLine();
                }
                fw.close();
            }catch(Exception ex){}
            JOptionPane.showMessageDialog(this, "The Attendance Has Been Submitted Successfully");
            this.setVisible(false);
        }
        else
        {
           JOptionPane.showMessageDialog(this, "The Attendance Is Already Exist.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


