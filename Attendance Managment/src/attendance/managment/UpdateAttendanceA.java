    public void init(String us)
    {
        userName=us;
        List<String> subjects = new ArrayList<String>();
         File subject= new File("subject.txt");
        try
        {
        Scanner fs= new Scanner(subject);
        while(fs.hasNext())
        {
             String tSub=fs.next();
             String tIntake=fs.next();
             String tLec=fs.next();
             if(tLec.equals(userName))
                  subjects.add(tSub);
        }
        fs.close();
        }catch(Exception ex){}
        jComboBox2.removeAllItems();
        List<String> aSubjects = new ArrayList<String>();
        File theDir = new File("ATT");
        attList=theDir.listFiles();
        for(int i=0;i<attList.length;i++)
        {
            String tempName=attList[i].getName();
            String[] tempSplit=tempName.split("_");
            if(!aSubjects.contains(tempSplit[0]))
                aSubjects.add(tempSplit[0]);
        }
        for(int i=0;i<aSubjects.size();i++)
            jComboBox2.addItem(aSubjects.get(i));
        try
        {
        loadDate();
        }catch(Exception ex){}
    }
    public void loadDate()
    {
        String selectedSubject=jComboBox2.getSelectedItem().toString();
        jComboBox3.removeAllItems();
        for(int i=0;i<attList.length;i++)
        {
            String tempName=attList[i].getName();
            String[] tempSplit=tempName.split("_");
            if(selectedSubject.equals(tempSplit[0]))
            {
                String date=tempSplit[1]+"/"+tempSplit[2]+"/"+tempSplit[3];
                boolean check=false;
                for(int j=0;j<jComboBox3.getItemCount();j++)
                {
                    if(jComboBox3.getItemAt(j).equals(date))
                        check=true;
                }
                if(!check)
                    jComboBox3.addItem(date);
            }
        }
        try
        {
        loadTime();
        }catch(Exception ex){}
    }
    public void loadTime()
    {
        String selectedSubject=jComboBox2.getSelectedItem().toString();
        String date=jComboBox3.getSelectedItem().toString().replace("/", "_");
        String partName=selectedSubject+"_"+date;
        jComboBox6.removeAllItems();
        
         for(int i=0;i<attList.length;i++)
        {
            String tempName=attList[i].getName();
            String[] tempSplit=tempName.split("_");
            if(attList[i].getName().contains(partName))
            {
                String time=tempSplit[4]+":"+tempSplit[5].replace(".txt","");
                jComboBox6.addItem(time);
            }
        }
        try
        {
         loadAttendance();
        }catch(Exception ex){}
    }
    public void loadAttendance()
    {
        String FileName=jComboBox2.getSelectedItem().toString()+"_"+
                jComboBox3.getSelectedItem().toString().replace("/","_")+"_"+
                jComboBox6.getSelectedItem().toString().replace(":","_")+".txt";
        FileName="ATT\\"+FileName;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        File Student= new File(FileName);
        try
        {       
            Scanner fs= new Scanner(Student);
            
            while(fs.hasNext())
            {
                String usNa=fs.next();
                String att=fs.next();
                String reason=fs.next();
                model.addRow(new Object[]{usNa, att});
            }
            fs.close();
        }catch(Exception ex){}
    }
