    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String time=jComboBox6.getSelectedItem().toString();
        time=time.replace(":","_");

        String FileName=jComboBox2.getSelectedItem().toString()+"_"+
        jComboBox3.getSelectedItem().toString().replace("/", "_")+"_"+
        time+".txt";

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        File att= new File("ATT"+"\\"+FileName);
            if(att.exists())
            {
                try
                {
                    BufferedWriter fw= new BufferedWriter(new FileWriter(att,false));
                    for(int i=0;i<jTable1.getRowCount();i++)
                    {
                            fw.write(model.getValueAt(i, 0)+" "+model.getValueAt(i, 1)+" "+"NONE");
                            fw.newLine();
                    }
                    fw.close();
                }catch(Exception ex){}
                JOptionPane.showMessageDialog(this, "The Attendance Has Been Updated Successfully");
                this.setVisible(false);
            }
            else
            {
                    JOptionPane.showMessageDialog(this, "The Attendance Does Not Exist.");
            }
    }//GEN-LAST:event_jButton4ActionPerformed

