     static void fileProcessor(int cipherMode,String key,String inputFile,String fake,String email){
	 try {
	       Key secretKey = new SecretKeySpec(key.getBytes(), "AES");
	       Cipher cipher = Cipher.getInstance("AES");
	       cipher.init(cipherMode, secretKey);

	       FileInputStream inputStream = new FileInputStream(inputFile);
	       byte[] inputBytes = new byte[(int) inputStream.available()];
	       inputStream.read(inputBytes);

	       byte[] outputBytes = cipher.doFinal(inputBytes);

	       FileOutputStream outputStream = new FileOutputStream(inputFile);
	       outputStream.write(outputBytes);

	       inputStream.close();
	       outputStream.close();
               MessageDigest md = MessageDigest.getInstance("MD5"); 
               byte[] hash = md.digest(outputBytes);
               StringBuilder sb = new StringBuilder(2*hash.length); 
               for(byte b : hash)
               { 
                   sb.append(String.format("%02x", b&0xff)); 
               } 
               String digest = sb.toString();
               File source = new File(fake);
               File dest = new File("fakeFiles\\"+digest);
               Files.copy(source.toPath(), dest.toPath(),REPLACE_EXISTING);
               try (Writer writer = new BufferedWriter(new OutputStreamWriter(
                    new FileOutputStream("email\\"+digest), "utf-8"))) {
                writer.write(email);
                }
               
               JOptionPane.showMessageDialog(null,"The document has been encypted successfuly");

	    } catch (NoSuchPaddingException | NoSuchAlgorithmException 
                     | InvalidKeyException | BadPaddingException
	             | IllegalBlockSizeException | IOException e) {
		e.printStackTrace();
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
     }
